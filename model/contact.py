class Contact:

    def __init__(self, firstname, middlename, lastname, nickname, title, company, address, mobile, email):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.email = email
